public class Calculadora2 {

	static double adicao (double A, double B) {
			double soma;
			soma = A + B;
			return soma; }
			
	static double subtracao (double A, double B) {
			double subtracao;
			subtracao = A - B;
			return subtracao; }
			
	static double multiplicacao (double A, double B) {
			double multiplicacao;
			multiplicacao = A * B;
			return multiplicacao; }
	
	static double divisao (double A, double B) {
			double divisao;
			divisao = A / B;
			return divisao; }
	
	static double restodivisao (double A, double B) {
			double restodivisao;
			restodivisao = A % B;
			return restodivisao; }
	
	}
	